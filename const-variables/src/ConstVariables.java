public class ConstVariables {
    public static void main(String[] args) {
        // Stating const Variables.

        // Constant variable names are usually written in uppercase
        // and if they have more than one word they are separated
        // with _.
        // To create a constant variable we have to use final keyboard.
        final int POSITION = -5;
        final int MAX_WIDTH = 9999;
        final int MIN_WIDTH = 1;
    }
}
