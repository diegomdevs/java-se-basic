public class NumericDataTypes {
    public static void main(String[] args) {
        // Stating Variables

        // Integer

        // byte is limited to three numbers.
        byte nB = 123;

        // short is limited to five numbers.
        short nS = 12345;

        // int is limited to ten numbers.
        int n = 1234567890;

        // log is limited to nineteen numbers.
        long nL = 1234567890121231233L;

        // Float

        // float
        float nF = 123.456F;

        // double
        double nD = 123.321313213213;
    }
}
